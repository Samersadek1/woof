-- Rebuild: join staged spans to bookings (pet + span fully inside stay).
-- end_date_exclusive from XLSX is [start, end) nights; bra uses inclusive end_date
-- (same as legacy import / single-night start=end rows).
-- One booking per span: narrowest matching stay when legacy duplicate bookings exist.
WITH resolved AS (
  SELECT DISTINCT ON (s.pet_source_external_id, s.room_number, s.start_date, s.end_date_exclusive)
    b.id                       AS booking_id,
    r.id                       AS room_id,
    s.start_date               AS start_date,
    s.end_date_exclusive - 1   AS end_date
  FROM _new_spans s
  JOIN pets        p  ON p.source_external_id = s.pet_source_external_id
  JOIN rooms       r  ON r.room_number = s.room_number
  JOIN booking_pets bp ON bp.pet_id = p.id
  JOIN bookings    b  ON b.id = bp.booking_id
                     AND s.start_date >= b.check_in_date
                     AND s.end_date_exclusive <= b.check_out_date
  ORDER BY
    s.pet_source_external_id,
    s.room_number,
    s.start_date,
    s.end_date_exclusive,
    (b.check_out_date - b.check_in_date),
    b.id
),
resolved_ok AS (
  SELECT * FROM resolved WHERE end_date >= start_date
),
ranked AS (
  SELECT
    booking_id,
    room_id,
    start_date,
    end_date,
    LEAD(start_date) OVER (
      PARTITION BY booking_id
      ORDER BY start_date, end_date, room_id
    ) AS next_start
  FROM resolved_ok
),
trimmed AS (
  SELECT
    booking_id,
    room_id,
    start_date,
    CASE
      WHEN next_start IS NOT NULL AND end_date >= next_start
      THEN next_start - 1
      ELSE end_date
    END AS end_date
  FROM ranked
)
INSERT INTO booking_room_assignments (booking_id, room_id, start_date, end_date)
SELECT booking_id, room_id, start_date, end_date
FROM trimmed
WHERE end_date >= start_date;

