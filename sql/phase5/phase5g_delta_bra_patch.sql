    WHEN NOT EXISTS (SELECT 1 FROM rooms WHERE room_number = s.room_number)
      THEN 'room not found in DB'
    ELSE 'no booking covers this pet on these dates'
  END AS reason
FROM _delta_spans s
WHERE NOT EXISTS (
  SELECT 1
  FROM pets p
  JOIN booking_pets bp ON bp.pet_id = p.id
  JOIN bookings b ON b.id = bp.booking_id
  JOIN rooms r ON r.room_number = s.room_number
  WHERE p.source_external_id = s.pet_source_external_id
    AND s.start_date >= b.check_in_date
    AND s.end_date_inclusive < b.check_out_date
);

